import { useQuery } from 'react-query';
import { useState } from 'react';

import UserInfoUpdateModal from './userInfoUpdateModal';

interface UserInfoDataType {
  bojId: string;
  introduceInfo: string;
}

export default function UserInfo() {
  const [userInfoUpdateModalFlag, setUserInfoUpdateModalFlag] =
    useState<boolean>(false);

  // 사용자 정보 받아오는 코드 작성하기
  const userInfoDataAxios: () => Promise<UserInfoDataType> = async () => {
    const response = await axiosInstance.get<UserInfoDataType>(
      `/members/info`,
      { withCredentials: true },
    );
    return response.data;
  };
  const { isLoading, error, data, refetch } = useQuery<UserInfoDataType>(
    'userInfoData',
    userInfoDataAxios,
  );

  if (isLoading) {
    return (
      <div className="flex flex-row justify-center items-center h-[15rem] w-[70rem] rounded-xl shadow-md">
        로딩중...
      </div>
    );
  }

  if (error) {
    return <div>에러 발생</div>;
  }

  return (
    <>
      {userInfoUpdateModalFlag ? (
        <UserInfoUpdateModal
          setUserInfoUpdateModalFlag={setUserInfoUpdateModalFlag}
          refetch={refetch}
          initialBojId={data?.bojId}
          initialOneLineIntro={data?.introduceInfo}
        />
      ) : null}
      <div className="flex flex-row justify-center h-[15rem] w-[70rem] rounded-xl shadow-md">
        <div className="flex flex-col justify-center w-[15rem]">
          <ProfileIcon />
        </div>
        <div className="flex flex-col justify-center w-[30rem]">
          {/* 사용자 닉네임 */}
          <Text size="2rem" bold="bold">
            {data?.bojId}
          </Text>

          {/* 사용자 한줄 소개/ */}
          <div>{data?.introduceInfo}</div>
          <Gap hSize="2rem" />

          <div className="flex flex-row">
            {/* 프로필 수정 버튼 */}
            <div
              className="flex flex-row items-center px-[1rem] py-[0.3rem] bg-gray-100 rounded cursor-pointer"
              onClick={() => {
                setUserInfoUpdateModalFlag(true);
                console.log('1324');
              }}
            >
              <ModifyIcon />
              <Gap wSize="0.5rem" />
              <div className="text-gray-600">프로필 수정</div>
            </div>
            <Gap wSize="1.5rem" />

            {/* 친구 목록 버튼 */}
            <div className="flex flex-row items-center px-[1rem] py-[0.3rem] border border-gray-300 rounded">
              <FriendIcon />
              <Gap wSize="0.5rem" />
              <div>친구 목록</div>
            </div>
          </div>
        </div>

        {/* 사용자 정보 아이콘들 */}
        <div className="flex flex-row w-[20rem] justify-end items-center">
          <EmailIcon />
          <Gap wSize="1rem" />

          <GithubIcon />
          <Gap wSize="1rem" />

          <LinkedinIcon />
          <Gap wSize="1rem" />

          <TistoryIcon />
        </div>
      </div>
    </>
  );
}
